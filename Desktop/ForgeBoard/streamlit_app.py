def _render_fg_blocker_reason(fg: dict) -> None:
    blocker_rows = _build_fg_blocker_rows(fg)
    with st.container(border=True):
        st.markdown("#### Why this FG cannot be fully made")
        if bool(fg.get("can_fulfill_demand")):
            st.success(
                "Current materials are sufficient to fully cover this finished good's net demand."
            )
            return

        if not blocker_rows and not fg.get("limiting_components"):
            st.warning("No BOM data was found for this finished good, so material blockers cannot be derived.")
            return

        if int(fg.get("recommended_build_qty", 0)) == 0:
            st.caption(
                "These limiting materials are the immediate raw-material blockers stopping production right now."
            )
        else:
            st.caption(
                "These limiting materials are the main reason this finished good can only be partially fulfilled."
            )

        if blocker_rows:
            st.dataframe(
                blocker_rows,
                use_container_width=True,
                hide_index=True,
            )
        else:
            st.info(
                "The finished good is constrained, but detailed shortage rows are not available for the limiting components."
            )


def _build_fg_blocker_rows(fg: dict) -> list[dict[str, object]]:
    shortages_by_component = {
        row["component"]: row for row in fg.get("shortages", []) if row.get("component")
    }
    rows: list[dict[str, object]] = []

    for component in fg.get("limiting_components", []):
        shortage = shortages_by_component.get(component)
        if shortage is None:
            continue

        qty_per_fg = float(shortage.get("qty_per_fg", 0.0) or 0.0)
        available = float(shortage.get("available_qty", 0.0) or 0.0)
        required = float(shortage.get("required_qty", 0.0) or 0.0)
        shortage_qty = float(shortage.get("shortage_qty", 0.0) or 0.0)
        possible_units = 0 if qty_per_fg <= 0 else max(int(math.floor((available / qty_per_fg) + 1e-9)), 0)
        reason = "Zero stock hard stop" if available <= 1e-9 else "Lowest build limit"

        rows.append(
            {
                "Component": component,
                "Qty / FG": round(qty_per_fg, 4),
                "Available": round(available, 2),
                "Possible FG Units": possible_units,
                "Required": round(required, 2),
                "Shortage": round(shortage_qty, 2),
                "Why blocking": reason,
            }
        )

    rows.sort(key=lambda item: (item["Possible FG Units"], -float(item["Shortage"])))
    return rows


def _fulfillment_outlook_card(fg: dict) -> str:
    net_demand = int(round(float(fg.get("net_demand_qty", 0))))
    build_now = int(fg.get("recommended_build_qty", 0))
    coverage = float(fg.get("coverage_ratio", 0.0))
    unmet = max(net_demand - build_now, 0)

    if net_demand == 0:
        tone = "success"
        kicker = "Demand already covered"
        body = (
            "There is no open net demand for this finished good. "
            "Current finished-good stock already covers the requirement."
        )
    elif bool(fg.get("can_fulfill_demand")):
        tone = "success"
        kicker = "Full fulfillment possible"
        body = (
            f"You can build all {net_demand} units needed right now and fully cover "
            "the current demand."
        )
    elif build_now > 0:
        tone = "warning"
        kicker = "Partial fulfillment"
        body = (
            f"You can build {build_now} out of {net_demand} units now, covering "
            f"{coverage:.0%} of demand. {unmet} units remain unmet."
        )
    else:
        tone = "alert"
        kicker = "Currently blocked"
        body = (
            f"You cannot build this finished good with current stock. Buildable now: "
            f"0 of {net_demand} units. Unmet demand remains {unmet} units."
        )

    return (
        f"<div class='fulfillment-card is-{html.escape(tone)}'>"
        f"<div class='fulfillment-kicker'>{html.escape(kicker)}</div>"
        f"<div class='fulfillment-body'>{html.escape(body)}</div>"
        "<div class='fulfillment-stats'>"
        f"<div class='fulfillment-stat'><span>Build now</span><strong>{build_now}</strong></div>"
        f"<div class='fulfillment-stat'><span>Unmet demand</span><strong>{unmet}</strong></div>"
        f"<div class='fulfillment-stat'><span>Fulfillment</span><strong>{coverage:.0%}</strong></div>"
        "</div>"
        "</div>"
    )


