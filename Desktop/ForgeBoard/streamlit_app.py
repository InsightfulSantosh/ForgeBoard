def _render_materials(result: dict) -> None:
    shortages = result.get("aggregate_shortages", [])
    usage_ranking = result.get("material_usage_ranking", [])
    enough_materials = [
        {
            "Component": row["component"],
            "Used In FG Count": row["used_in_fg_count"],
            "Used In FGs": ", ".join(row["used_in_fgs"]),
            "Total Required Qty": round(row["total_required_qty"], 2),
            "Available Qty": round(row["available_qty"], 2),
            "Surplus Qty": round(
                max(float(row["available_qty"]) - float(row["total_required_qty"]), 0.0),
                2,
            ),
        }
        for row in usage_ranking
        if float(row.get("available_qty", 0.0)) + 1e-9 >= float(row.get("total_required_qty", 0.0))
    ]

    left, right = st.columns([1.45, 0.9])
    with left:
        with st.container(border=True):
            st.markdown("### Aggregate shortages")
            if shortages:
                st.dataframe(
                    [
                        {
                            "Component": row["component"],
                            "Required Qty": round(row["required_qty"], 2),
                            "Available Qty": round(row["available_qty"], 2),
                            "Shortage Qty": round(row["shortage_qty"], 2),
                        }
                        for row in shortages
                    ],
                    use_container_width=True,
                    hide_index=True,
                )
            else:
                st.success("No shortages detected in the current scenario.")
    with right:
        with st.container(border=True):
            st.markdown("### Procurement ranking")
            if shortages:
                st.caption("All shortage components ranked by shortage magnitude.")
                st.markdown(
                    _badge_row([row["component"] for row in shortages], "accent"),
                    unsafe_allow_html=True,
                )
            else:
                st.info("No shortage-driven procurement ranking is needed in the current scenario.")

    with st.container(border=True):
        st.markdown("### Raw material importance by use")
        st.caption(
            "Ranked by how broadly a raw material is used across finished goods and how much total quantity the current scenario requires."
        )
        if usage_ranking:
            st.dataframe(
                [
                    {
                        "Component": row["component"],
                        "Used In FG Count": row["used_in_fg_count"],
                        "Used In FGs": ", ".join(row["used_in_fgs"]),
                        "Total Required Qty": round(row["total_required_qty"], 2),
                        "Planned Consumption Qty": round(row["planned_consumption_qty"], 2),
                        "Available Qty": round(row["available_qty"], 2),
                        "Shortage Qty": round(row["shortage_qty"], 2),
                        "Usage Importance Score": row["usage_importance_score"],
                    }
                    for row in usage_ranking
                ],
                use_container_width=True,
                hide_index=True,
            )
        else:
            st.info("No material-usage ranking is available for the current scenario.")

    with st.container(border=True):
        st.markdown("### Materials available in enough quantity")
        st.caption(
            "These materials already have enough available stock to cover the full current scenario requirement."
        )
        if enough_materials:
            st.dataframe(
                enough_materials,
                use_container_width=True,
                hide_index=True,
            )
        else:
            st.info("No fully covered materials were identified in the current scenario.")


def _render_assistant(
    result: dict,
    use_llm: bool,
    llm_model: str,
) -> None:
    st.markdown("### Planner assistant")
    history = st.session_state.setdefault("chat_history", [])
    prompt_col, action_col = st.columns([1.3, 0.22])
    with prompt_col:
        question = st.text_input(
            "Ask a planner question",
            placeholder="Example: Why is FG03 blocked?",
            label_visibility="visible",
        )
    with action_col:
        st.markdown("<div class='button-spacer'></div>", unsafe_allow_html=True)
        ask_clicked = st.button("Ask", use_container_width=True)

    if ask_clicked and question.strip():
        answer = PlanningAssistant().answer(
            question.strip(),
            result,
            use_llm=use_llm,
            llm_model=llm_model,
        ).to_dict()
        history.append(answer)
        st.session_state.chat_history = history

    for item in history:
        with st.container(border=True):
            st.caption(f"{item['mode']} | {item['intent']}")
            st.markdown(f"**Q:** {item['question']}")
            st.write(item["answer"])


